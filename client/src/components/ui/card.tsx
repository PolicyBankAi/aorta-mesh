import * as React from "react";
import { cn } from "@/lib/utils";

/**
 * Card (AORTA Mesh hardened)
 * --------------------------
 * - Cyan/black theme
 * - Accessible role + labelling
 * - Testable with data-testid
 */

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    role="group"
    className={cn(
      "rounded-lg border border-cyan-400 bg-black text-cyan-200 shadow-md",
      className
    )}
    data-testid="card"
    {...props}
  />
));
Card.displayName = "Card";

